package Operators;

public class Comparison_Operators {


    public static void main(String[] args) {
         int x = 1;
         int y = 1;
        System.out.println(x==y);    //The code sout(x==y,  x!=y etc.) are boolean expressions because they produces a boolean value (Either True or False).
        System.out.println(x!=y);
        System.out.println(x>y);
        System.out.println(x>=y);
        System.out.println(x<y);
        System.out.println(x<=y);
    }
}
