public class Maximum_and_Minimum {

    public static void main(String[] args) {

        // If we want to find the Largest of 2 numbers, We use the "Math.max()".

        int A = 67;
        int B = 90;

        System.out.println("The largest number is : " + Math.max(A , B));

       // If we want to find the Smallest of 2 numbers, We use the "Math.min()".

        double X = 63;
        double Y = 78;

        System.out.println("\n The smallest number is : " + Math.min(X , Y));


           // We can also use other functionalities like...
          //  Math.sqrt to find the square root of a given number.
         //   Math.abs to find the absolute value of a double.


    }
}
